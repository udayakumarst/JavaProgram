package JavaClass;
public class Operators {
	public static void main(String[] args) 
	{
	  int u = 200;
	  int a = 300;
	  int sum = u+a;
	  System.out.println(sum);
	  int sub = a-u;
	  System.out.println(sub);
	  int mult = u*a;
	  System.out.println(mult);
	  int divided = u/a;
	  System.out.println(divided);
	  int per = a%u;
	  System.out.println(per);
	}
}
